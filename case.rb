fav_color = "blue"

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Orange like, well... an orange."
when 'yellow'
	puts "Yellow daffodils are so pretty in the spring!"
when 'green'
	puts "Have you been to the Emarald City in Oz?"
when 'blue'
	puts "Blue like the sky!"
when 'purple'
	puts "Purple plums are the tastiest."
else
	puts "Hmm, well I don't know what the color is!"
end



